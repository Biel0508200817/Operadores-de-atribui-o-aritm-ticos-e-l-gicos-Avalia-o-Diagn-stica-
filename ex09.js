/**
 * Explique o resultado das expressões:


console.log("5" - 2);
console.log("5" + 2);
console.log(true + 1);
console.log(false + 10);

 */

console.log("5" - 2); // 3
// Justificativa: O operador de subtração (-) força a conversão da string "5" para um número. Portanto, 5 - 2 resulta em 3.

console.log("5" + 2); // "52"
// Justificativa: O operador de adição (+) entre uma string e um número resulta na concatenação das duas. Portanto, "5" + 2 resulta em "52".

console.log(true + 1); // 2
// Justificativa: O valor booleano true é convertido para 1, então 1 + 1 resulta em 2.

console.log(false + 10); // 10
// Justificativa: O valor booleano false é convertido para 0, então 0 + 10 resulta em 10.
