/**
 * Dada a variável `x = 10`, utilize os operadores de atribuição composta para realizar as seguintes operações e exibir os resultados:

- `x += 5`
- `x -= 3`
- `x *= 2`
- `x /= 4`
- `x %= 3`
 */

var x = 10;

x += 5;
console.log("x após x += 5: ", x);

x -= 3;
console.log("x após x -= 3: ", x);

x *= 2;
console.log("x após x *= 2: ", x);

x /= 4;
console.log("x após x /= 4: ", x);

x %= 3;
console.log("x após x %= 3: ", x);